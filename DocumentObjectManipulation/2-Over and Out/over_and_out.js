//Specifies what happens when the user's mouse hover over the div element with id = over_and_out
function changetoBlack() {
    //Set the element background color to black when mouse is over the div
    document.getElementById("over_and_out").style.backgroundColor = "black";
}

//Specifies what happens when the user's mouse leaves the div element with id = over_and_out
function changetoBlue() {
     //Set the element background color to black when mouse leaves the div
    document.getElementById("over_and_out").style.backgroundColor = "blue";
}